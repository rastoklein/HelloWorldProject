package basicexample;

public class LoopExamples {
    public static void main(String[] args) {

        //vypis cisla od 1 do 10
        //vyuzijeme prikaz for

        for (int i=1; i<=10; i+=2){    //i+=2 je to iste ako i=i+2
            System.out.println(i);

        }
        System.out.println("");
        for (int i=1; i<=10; i*=2){    //i+=2 je to iste ako i=i+2
            System.out.println(i);

        }
    }
}
